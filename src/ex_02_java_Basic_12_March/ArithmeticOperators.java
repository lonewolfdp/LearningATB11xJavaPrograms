package ex_02_java_Basic_12_March;

public class ArithmeticOperators {
    public static void main(String[] args) {

        //Arithmetic operations

        int a=20, b=5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }


}
